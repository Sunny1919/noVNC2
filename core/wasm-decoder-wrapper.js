/*
 * WebAssembly Decoder Wrapper
 * JavaScript wrapper cho WASM decoder
 */

import * as Log from './util/logging.js';

export class WasmDecoderWrapper {
    constructor() {
        this.module = null;
        this.isReady = false;
        this.isLoading = false;
        this.loadPromise = null;
        
        // Function pointers
        this.decodeRaw = null;
        this.decodeRRE = null;
        this.decodeHextile = null;
        this.fastCopy = null;
        this.rgbToRgba = null;
        this.rgbaToRgb = null;
        this.bgrToRgb = null;
    }

    // Load WASM module
    async load(wasmPath = './decoder.wasm') {
        if (this.isReady) {
            return true;
        }

        if (this.isLoading) {
            return this.loadPromise;
        }

        this.isLoading = true;
        this.loadPromise = this._loadModule(wasmPath);
        
        try {
            await this.loadPromise;
            return true;
        } catch (err) {
            Log.Error('Failed to load WASM module:', err);
            return false;
        } finally {
            this.isLoading = false;
        }
    }

    async _loadModule(wasmPath) {
        try {
            // Import WASM module (generated by Emscripten or mock)
            const WasmDecoderModule = await import('./decoder.js');
            
            // Handle both default export and named export
            const WasmDecoder = WasmDecoderModule.default || WasmDecoderModule.WasmDecoder;
            
            if (!WasmDecoder) {
                throw new Error('WasmDecoder not found in module');
            }
            
            // Initialize module
            this.module = await WasmDecoder({
                locateFile: (path) => {
                    if (path.endsWith('.wasm')) {
                        return wasmPath;
                    }
                    return path;
                }
            });

            // Check if this is a mock module
            if (this.module.isMock) {
                Log.Info('WASM decoder not available (using mock), falling back to JS decoders');
                Log.Info('To build real WASM: cd wasm && build.bat (or ./build.sh)');
                return;
            }

            // Wrap functions
            this.decodeRaw = this.module.cwrap('decodeRaw', null, 
                ['number', 'number', 'number', 'number', 'number']);
            
            this.decodeRRE = this.module.cwrap('decodeRRE', 'number', 
                ['number', 'number', 'number', 'number', 'number']);
            
            this.decodeHextile = this.module.cwrap('decodeHextile', 'number', 
                ['number', 'number', 'number', 'number', 'number']);
            
            this.fastCopy = this.module.cwrap('fastCopy', null, 
                ['number', 'number', 'number']);
            
            this.rgbToRgba = this.module.cwrap('rgbToRgba', null, 
                ['number', 'number', 'number']);
            
            this.rgbaToRgb = this.module.cwrap('rgbaToRgb', null, 
                ['number', 'number', 'number']);
            
            this.bgrToRgb = this.module.cwrap('bgrToRgb', null, 
                ['number', 'number', 'number']);

            this.isReady = true;
            Log.Info('WASM decoder loaded successfully');

        } catch (err) {
            Log.Error('Failed to initialize WASM module:', err);
            Log.Info('Falling back to JavaScript decoders');
            throw err;
        }
    }

    // Decode raw encoding
    decodeRawEncoding(srcData, width, height, bytesPerPixel) {
        if (!this.isReady) {
            throw new Error('WASM module not loaded');
        }

        const dstSize = width * height * bytesPerPixel;
        
        // Allocate memory
        const srcPtr = this.module._malloc(srcData.length);
        const dstPtr = this.module._malloc(dstSize);

        try {
            // Copy source data to WASM memory
            this.module.HEAPU8.set(srcData, srcPtr);

            // Decode
            this.decodeRaw(srcPtr, dstPtr, width, height, bytesPerPixel);

            // Copy result back
            const result = new Uint8Array(dstSize);
            result.set(this.module.HEAPU8.subarray(dstPtr, dstPtr + dstSize));

            return result;

        } finally {
            // Free memory
            this.module._free(srcPtr);
            this.module._free(dstPtr);
        }
    }

    // Decode RRE encoding
    decodeRREEncoding(srcData, width, height, bytesPerPixel) {
        if (!this.isReady) {
            throw new Error('WASM module not loaded');
        }

        const dstSize = width * height * bytesPerPixel;
        
        const srcPtr = this.module._malloc(srcData.length);
        const dstPtr = this.module._malloc(dstSize);

        try {
            this.module.HEAPU8.set(srcData, srcPtr);
            
            const bytesRead = this.decodeRRE(srcPtr, dstPtr, width, height, bytesPerPixel);

            const result = new Uint8Array(dstSize);
            result.set(this.module.HEAPU8.subarray(dstPtr, dstPtr + dstSize));

            return { data: result, bytesRead };

        } finally {
            this.module._free(srcPtr);
            this.module._free(dstPtr);
        }
    }

    // Decode Hextile encoding
    decodeHextileEncoding(srcData, width, height, bytesPerPixel) {
        if (!this.isReady) {
            throw new Error('WASM module not loaded');
        }

        const dstSize = width * height * bytesPerPixel;
        
        const srcPtr = this.module._malloc(srcData.length);
        const dstPtr = this.module._malloc(dstSize);

        try {
            this.module.HEAPU8.set(srcData, srcPtr);
            
            const bytesRead = this.decodeHextile(srcPtr, dstPtr, width, height, bytesPerPixel);

            const result = new Uint8Array(dstSize);
            result.set(this.module.HEAPU8.subarray(dstPtr, dstPtr + dstSize));

            return { data: result, bytesRead };

        } finally {
            this.module._free(srcPtr);
            this.module._free(dstPtr);
        }
    }

    // Fast copy
    fastCopyData(srcData, dstData) {
        if (!this.isReady) {
            throw new Error('WASM module not loaded');
        }

        const srcPtr = this.module._malloc(srcData.length);
        const dstPtr = this.module._malloc(srcData.length);

        try {
            this.module.HEAPU8.set(srcData, srcPtr);
            this.fastCopy(srcPtr, dstPtr, srcData.length);
            dstData.set(this.module.HEAPU8.subarray(dstPtr, dstPtr + srcData.length));
        } finally {
            this.module._free(srcPtr);
            this.module._free(dstPtr);
        }
    }

    // Convert RGB to RGBA
    convertRgbToRgba(rgbData, pixels) {
        if (!this.isReady) {
            throw new Error('WASM module not loaded');
        }

        const srcPtr = this.module._malloc(rgbData.length);
        const dstPtr = this.module._malloc(pixels * 4);

        try {
            this.module.HEAPU8.set(rgbData, srcPtr);
            this.rgbToRgba(srcPtr, dstPtr, pixels);

            const result = new Uint8Array(pixels * 4);
            result.set(this.module.HEAPU8.subarray(dstPtr, dstPtr + pixels * 4));
            return result;

        } finally {
            this.module._free(srcPtr);
            this.module._free(dstPtr);
        }
    }

    // Convert RGBA to RGB
    convertRgbaToRgb(rgbaData, pixels) {
        if (!this.isReady) {
            throw new Error('WASM module not loaded');
        }

        const srcPtr = this.module._malloc(rgbaData.length);
        const dstPtr = this.module._malloc(pixels * 3);

        try {
            this.module.HEAPU8.set(rgbaData, srcPtr);
            this.rgbaToRgb(srcPtr, dstPtr, pixels);

            const result = new Uint8Array(pixels * 3);
            result.set(this.module.HEAPU8.subarray(dstPtr, dstPtr + pixels * 3));
            return result;

        } finally {
            this.module._free(srcPtr);
            this.module._free(dstPtr);
        }
    }

    // Convert BGR to RGB
    convertBgrToRgb(data, pixels, bytesPerPixel) {
        if (!this.isReady) {
            throw new Error('WASM module not loaded');
        }

        const ptr = this.module._malloc(data.length);

        try {
            this.module.HEAPU8.set(data, ptr);
            this.bgrToRgb(ptr, pixels, bytesPerPixel);
            data.set(this.module.HEAPU8.subarray(ptr, ptr + data.length));
        } finally {
            this.module._free(ptr);
        }
    }

    // Check if WASM is supported
    static isSupported() {
        return typeof WebAssembly !== 'undefined';
    }

    // Benchmark decoder
    async benchmark(iterations = 100) {
        if (!this.isReady) {
            await this.load();
        }

        const width = 1024;
        const height = 768;
        const bytesPerPixel = 4;
        const testData = new Uint8Array(width * height * bytesPerPixel);

        // Fill with test pattern
        for (let i = 0; i < testData.length; i++) {
            testData[i] = i % 256;
        }

        const results = {};

        // Benchmark Raw
        const rawStart = performance.now();
        for (let i = 0; i < iterations; i++) {
            this.decodeRawEncoding(testData, width, height, bytesPerPixel);
        }
        const rawEnd = performance.now();
        results.raw = {
            totalTime: rawEnd - rawStart,
            avgTime: (rawEnd - rawStart) / iterations,
            fps: 1000 / ((rawEnd - rawStart) / iterations)
        };

        Log.Info('WASM Decoder Benchmark:', results);
        return results;
    }
}

// Singleton instance
let wasmDecoderInstance = null;

export function getWasmDecoder() {
    if (!wasmDecoderInstance) {
        wasmDecoderInstance = new WasmDecoderWrapper();
    }
    return wasmDecoderInstance;
}
